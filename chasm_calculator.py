    def run(self):
        """Abre o diálogo, carrega SHP do ZIP e recolhe parâmetros do sDNA."""
        if self.first_start is True or self.dlg is None:
            self.first_start = False
            self.dlg = ChasmDialog(self.iface.mainWindow())

        # mostra o diálogo
        self.dlg.show()
        result = self.dlg.exec_()
        if not result:
            return  # cancelado

        # ---------- 1) Carregar SHP de dentro do ZIP ----------
        zip_path = self.dlg.selected_zip()
        shp_inside = self.dlg.selected_shp1()

        if not zip_path:
            QMessageBox.warning(self.iface.mainWindow(), "Chasm", "Selecione um arquivo .zip.")
            return
        if not os.path.isfile(zip_path):
            QMessageBox.warning(self.iface.mainWindow(), "Chasm", "Arquivo .zip inválido ou inexistente.")
            return
        if not shp_inside:
            QMessageBox.warning(self.iface.mainWindow(), "Chasm", "Selecione um shapefile dentro do .zip.")
            return

        # Normaliza barras no Windows para o GDAL (/vsizip aceita '/')
        norm_zip = os.path.normpath(zip_path).replace('\\', '/')
        vsi_path = f"/vsizip/{norm_zip}/{shp_inside}"
        layer_name = os.path.splitext(os.path.basename(shp_inside))[0]

        vlayer = QgsVectorLayer(vsi_path, layer_name, "ogr")
        if not vlayer or not vlayer.isValid():
            QgsMessageLog.logMessage(f"Falha ao carregar layer: {vsi_path}", "Chasm", Qgis.Critical)
            QMessageBox.critical(self.iface.mainWindow(), "Chasm", "Não foi possível carregar o shapefile do .zip.")
            return

        # evita duplicar: se já existe mesma fonte+nome, não adiciona
        add_layer = True
        for lyr in QgsProject.instance().mapLayers().values():
            try:
                if lyr.source() == vlayer.source() and lyr.name() == vlayer.name():
                    add_layer = False
                    break
            except Exception:
                pass

        if add_layer:
            QgsProject.instance().addMapLayer(vlayer)
            QgsMessageLog.logMessage(f"Camada adicionada: {layer_name}", "Chasm", Qgis.Info)

        # ---------- 2) Pegar camada de rede escolhida ----------
        net_layer = self.dlg.selected_network_layer()
        if net_layer is None:
            QMessageBox.warning(self.iface.mainWindow(), "Chasm", "Selecione a camada de sistema viário (linhas).")
            return

        # ---------- 3) Parâmetros do sDNA (do diálogo) ----------
        params = self.dlg.sdna_params()
        QgsMessageLog.logMessage(f"sDNA params: {params}", "Chasm", Qgis.Info)

        # valida 4 destinos
        dest_weights = [w for w in params.get("dest_weights", []) if w]
        if len(dest_weights) != 4:
            QMessageBox.warning(
                self.iface.mainWindow(),
                "Chasm",
                "Preencha os quatro 'Destination weight' (interesse/existente, outros/existente, interesse/NS, outros/NS)."
            )
            return

